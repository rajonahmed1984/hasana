                    		    তিনি তোমাদের জন্যে অসংখ্য তারকা বানিয়ে রেখেছেন যেন তোমরা তা দিয়ে জলে-স্থলের আঁধারে পথের দিশা পেতে পারো, যে সম্প্রদায়ের লোকেরা (এসব রহস্যের কথা) জানে, তাদের জন্যে আমি আমার নিদর্শনসমূহ খুলে খুলে বর্ণনা করেছি। 
