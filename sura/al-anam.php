                    		</li>
                            <div class="border-line"></div>
                            <li>
                    		    তিনি আসমান থেকে পানি (-র ধারা) নাযিল করেন, অতপর সে পানি দিয়ে আমি সব রকমের উদ্ভিদ (ও গাছপালা) জন্মানোর ব্যবস্থা করি, তা থেকে সবুজ শ্যামল পাতা উদগত করি, (পরে) তা থেকে আমি পরস্পর জড়ানো ঘন শস্যদানা সৃষ্টি করি এবং (ফলের) ভারে নুয়ে পড়া খেজুরের গোছা বের করে আনি, আংগুরের উদ্যানমালা, জলপাই ও আনার পয়দা করি, এগুলো একে অন্যের সদৃশ হয়, আবার (একটার সাথে) আরেকটার গরমিলও থাকে; গাছ যখন সুশোভিত হয় তখন (এক সময়) তা ফলবান হয়, যখন ফলগুলো পাকতে শুরু করে, তখন তোমরা এই সৃষ্টি-নৈপুণ্য দেখো; অবশ্যই এতে ঈমানদার লোকদের জন্যে বহু নিদর্শন রয়েছে। 
