                            <div class="border-line"></div>
                            <li>
                    		    সে ব্যক্তির চাইতে বড় যালেম আর কে আছে যে আল্লাহ তায়ালার ওপর মিথ্যা আরোপ করে, অথবা বলে, আমার ওপর ওহী নাযিল হয়েছে, (যদিও) তার ওপর কিছুই নাযিল করা হয়নি, (তার চাইতেই বা বড় যালেম কে,) যে বলে, আমি অচিরেই আল্লাহর নাযিল করা গ্রন্থের মতো কিছু নাযিল করে দেখাবো! যদি (সত্যি সত্যিই) যালেমদের মৃত্যু-যন্ত্রণা (উপস্থিত) হবার সময়টা তুমি দেখতে পেতে! যখন (মৃত্যুর) ফেরেশতারা তাদের দিকে হাত বাড়িয়ে বলবে, তোমাদের প্রাণবায়ু বের করে দাও; তোমরা আল্লাহ তায়ালা সম্পর্কে যেসব অন্যায় কথা বলতে এবং তাঁর ব্যাপারে যে (ক্ষমাহীন) ঔদ্ধত্য প্রকাশ করতে, তার বিনিময়ে আজ অত্যন্ত অবমাননাকর এক আযাব তোমাদের দেয়া হবে। 
                    		</li>
                            <div class="border-line"></div>
                            <li>
                    		    (আজ) তোমরা আমার সামনে নিসঙ্গ অবস্থায় এলে, যেমনি নিসঙ্গ অবস্থায় আমি তোমাদের প্রথমবার সৃষ্টি করেছিলাম, অতপর তোমাদের আমি যা কিছু (বিষয় সম্পদ) দান করেছি, তার সবটুকুই তোমরা পেছনে ফেলে এসেছো, তোমাদের সাথে তোমাদের সুপারিশকারী ব্যক্তিদের– যাদের তোমরা মনে করতে তারা তোমাদের (কাজকর্মের) মাঝে অংশীদার– (কই) তাদের তো (আজ) তোমাদের মাঝে দেখতে পাচ্ছি না! বস্তুত তাদের এবং তোমাদের মধ্যকার সেই সম্পর্ক ছিন্ন ভিন্ন হয়ে গেছে এবং তাদের ব্যাপারে তোমরা যা ধারণা করতে তাও আজ নিষ্ফল হয়ে গেছে। 
